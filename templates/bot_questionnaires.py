# Keywords (question TEXT or hint) that indicate personal contact data — answers
# are stripped by _redact_sensitive before reaching OpenRouter (152-ФЗ minimization).
# Text matches:
#   "ваш telegram"       → seller Q6  "Ваш Telegram для получения заявок"
#   "telegram менеджера" → support Q5 "Telegram менеджера для эскалации"
# Hint matches (is_sensitive_question now checks hint too):
#   "telegram username"  → service_orders Q8, coach Q13, hr Q9 hints
_SENSITIVE_MARKERS = (
    "токен",
    "api ключ",
    "api-ключ",
    "api key",
    "ваш telegram",          # personal Telegram handle of bot owner (text)
    "telegram менеджера",    # manager Telegram handle (text)
    "telegram username",     # service_orders Q8, coach Q13, hr Q9 hints
    "номер телефона",        # phone contact
    "whatsapp",              # WhatsApp contact
    "вотсап",                # WhatsApp (alternative spelling)
)


def is_sensitive_question(text: str, hint: str = "") -> bool:
    """True if the question (or its hint) asks for a credential or personal contact
    that must never reach an LLM."""
    combined = (text + " " + hint).lower()
    return any(marker in combined for marker in _SENSITIVE_MARKERS)
