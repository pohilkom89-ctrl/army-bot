_SENSITIVE_MARKERS = ("токен", "api ключ", "api-ключ", "api key")


def is_sensitive_question(text: str) -> bool:
    """True if the question asks for a credential that must never reach an LLM."""
    low = text.lower()
    return any(marker in low for marker in _SENSITIVE_MARKERS)
